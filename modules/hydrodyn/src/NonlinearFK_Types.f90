   call RegPack(RF, InData%nBody)
   call RegPack(RF, allocated(InData%Bodies))
   if (allocated(InData%Bodies)) then
      call RegPackBounds(RF, 1, lbound(InData%Bodies), ubound(InData%Bodies))
      LB(1:1) = lbound(InData%Bodies)
      UB(1:1) = ubound(InData%Bodies)
      do i1 = LB(1), UB(1)
         call NonlinearFK_PackBodyType(RF, InData%Bodies(i1)) 
      end do
   end if
   call RegPackAlloc(RF, InData%FKMod)
   call RegPack(RF, associated(InData%WaveField))
   if (associated(InData%WaveField)) then
      call RegPackPointer(RF, c_loc(InData%WaveField), PtrInIndex)
      if (.not. PtrInIndex) then
         call SeaSt_WaveField_PackSeaSt_WaveFieldType(RF, InData%WaveField) 
      end if
   end if
   if (RegCheckErr(RF, RoutineName)) return
end subroutine

subroutine NonlinearFK_UnPackParam(RF, OutData)
   type(RegFile), intent(inout)    :: RF
   type(NonlinearFK_ParameterType), intent(inout) :: OutData
   character(*), parameter            :: RoutineName = 'NonlinearFK_UnPackParam'
   integer(B4Ki)   :: i1
   integer(B4Ki)   :: LB(1), UB(1)
   integer(IntKi)  :: stat
   logical         :: IsAllocAssoc
   integer(B8Ki)   :: PtrIdx
   type(c_ptr)     :: Ptr
   if (RF%ErrStat /= ErrID_None) return
   call RegUnpack(RF, OutData%nBody); if (RegCheckErr(RF, RoutineName)) return
   if (allocated(OutData%Bodies)) deallocate(OutData%Bodies)
   call RegUnpack(RF, IsAllocAssoc); if (RegCheckErr(RF, RoutineName)) return
   if (IsAllocAssoc) then
      call RegUnpackBounds(RF, 1, LB, UB); if (RegCheckErr(RF, RoutineName)) return
      allocate(OutData%Bodies(LB(1):UB(1)),stat=stat)
      if (stat /= 0) then 
         call SetErrStat(ErrID_Fatal, 'Error allocating OutData%Bodies.', RF%ErrStat, RF%ErrMsg, RoutineName)
         return
      end if
      do i1 = LB(1), UB(1)
         call NonlinearFK_UnpackBodyType(RF, OutData%Bodies(i1)) ! Bodies 
      end do
   end if
   call RegUnpackAlloc(RF, OutData%FKMod); if (RegCheckErr(RF, RoutineName)) return
   if (associated(OutData%WaveField)) deallocate(OutData%WaveField)
   call RegUnpack(RF, IsAllocAssoc); if (RegCheckErr(RF, RoutineName)) return
   if (IsAllocAssoc) then
      call RegUnpackPointer(RF, Ptr, PtrIdx); if (RegCheckErr(RF, RoutineName)) return
      if (c_associated(Ptr)) then
         call c_f_pointer(Ptr, OutData%WaveField)
      else
